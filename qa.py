"""
qa.py — QA system that reads annotated markdown and answers user questions.

Key behavior change from the old verifier:
    - Reads hsbc_annotated.md (generated by verifier.py) instead of plain hsbc.md
    - Parses VERIFY comment tags from each table block
    - For CORRECT tables: answer directly from the annotated markdown
    - For INCORRECT tables: skip direct answer, return page image(s) from PDF_REF

The annotated markdown format:
    <!-- VERIFY: CORRECT | confidence=0.94 -->
    <!-- PDF_REF: page=5, section="Section C: Fees and Charges" -->
    | Fee Type         | Amount      | ...
    ...

    <!-- VERIFY: INCORRECT | reason=cell_boundary_bleed | confidence=0.61 -->
    <!-- PDF_REF: page=3, section="Tips to save you time and effort" -->
    | Service | **HSBC Mobile Banking** | ...
    ...
"""

